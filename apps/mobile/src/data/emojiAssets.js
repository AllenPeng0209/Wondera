export const emojiByKey = Object.fromEntries(
  Object.entries(emojiByDir).flatMap(([dir, files]) =>
    Object.entries(files).map(([fileName, module]) => [`${dir}/${fileName}`, module])
  )
);

function keysForDir(dir) {
  const files = emojiByDir[dir];
  if (!files) return [];
  return Object.keys(files).map((fileName) => `${dir}/${fileName}`);
}

